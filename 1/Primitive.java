public class Primitive {
    public static void main(String[] args) {
        boolean b=true;
        char c='A';
        byte bi=Byte.MAX_VALUE;
        short s=Short.MAX_VALUE;
        int i=Integer.MAX_VALUE;
        long l=Long.MAX_VALUE;
        float f=Float.MAX_VALUE;
        double d=Double.MAX_VALUE;
        System.out.println(b+" is boolean");
        System.out.println(c+" is character");
        System.out.println(bi+" is byte");
        System.out.println(s+" is short");
        System.out.println(i+" is integer");
        System.out.println(l+" is long");
        System.out.println(f+" is float");
        System.out.println(d+" is double");
        System.out.println("Tanmay Trivedi (22TCE540)");
    }    
}